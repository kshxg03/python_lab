num_1 = 7
num_2 = 3.5

print(type(num_1))
print(type(num_2))

sum = num_1 + num_2
print(sum)

diff = num_1 - num_2
print(diff)

prod = num_1 * num_2
print(prod)

div = num_1/num_2
print(div)

num_1 = str(num_1)
num_2 = str(num_2)
sum = num_1 + num_2
print(sum)

print("")

